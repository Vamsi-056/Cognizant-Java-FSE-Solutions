public class DataTypedemonstration {
    public static void main(String[] args){
        
        int num1=10;
        float num2=5.0f;
        double num3=6.5;
        char ch='a';
        boolean flag=true;
        System.out.println("This is a integer type "+num1);
        System.out.println("This is a float type "+num2);
        System.out.println("This is a double type "+num3);
        System.out.println("This is a character type "+ch);
        System.out.println("This is a boolean type "+flag);

    }
}
